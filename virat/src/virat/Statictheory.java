package virat;

public class Statictheory {

	public static void main(String[] args) {
		System.out.println("hello world");
		Statictheory st=new Statictheory();
		st.ajay("hehehe");
	}

	public static void ajay(){
		System.out.println("hi ajay");
	}
	
	
	public void kumar(){
		
		System.out.println("kumar");
	}
	
	public void ajay(String s){
		System.out.println(s);
	}
	

	public void kumar(String k){
		
		System.out.println(k);
	}
}
